package org.compareschool.domain;

public class MasterProperty {

	private long mpropId;
	private String mpropText;
	private String mpropDesc;

	public long getMpropId() {
		return mpropId;
	}

	public void setMpropId(long mpropId) {
		this.mpropId = mpropId;
	}

	public String getMpropText() {
		return mpropText;
	}

	public void setMpropText(String mpropText) {
		this.mpropText = mpropText;
	}

	public String getMpropDesc() {
		return mpropDesc;
	}

	public void setMpropDesc(String mpropDesc) {
		this.mpropDesc = mpropDesc;
	}

}
